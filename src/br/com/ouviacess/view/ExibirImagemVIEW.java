     private void mostraFoto(){
       try {
            LabelImagem.setText("");

            requerimentosDTO.setId_requerimento(this.id_requerimento);
            ResultSet rs = requerimentosCTR.consultarImagem(requerimentosDTO, 1);
            if (rs.next()) {
                nome.setText(rs.getString("nome"));
                ManipularImagemVIEW.exibiImagemLabel(rs.getBytes("dados_arquivo"), LabelImagem);
            } else {
                JOptionPane.showMessageDialog(null, "Imagemagem n√£o encontrada!!!");
                nome.setText("");
                LabelImagem.setText("");
                LabelImagem.setIcon(new ImageIcon(""));
            }

        } catch (Exception ex) {
            System.out.println("Erro: " + ex.getMessage());
        } finally {
            requerimentosCTR.CloseDB();
        }
    }
    
